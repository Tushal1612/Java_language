package Basic_Cheeze;

public class VariablesBasics {
    static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        int y;  //Declaration
        y = 20; //initialization
        System.out.println(y);
        x = 7;  //updation
        System.out.println(x);
        x = x + 7;
        System.out.println(x);
    }
}
