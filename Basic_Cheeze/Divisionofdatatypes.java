package Basic_Cheeze;

public class Divisionofdatatypes {
    static void main(String[] args) {
        double x =5/2;
        System.out.println(x); //Ans = 2.0 // int/int = int

        double y =5.0/2.0;
        System.out.println(y); //Ans = 2.0 // double/double = double

        double z =5/2.0;
        System.out.println(z); //Ans = 2.0 // int/double = Double

        double k =5.0/2;
        System.out.println(k); //Ans = 2.0 // Double/int = Double





        //  int/int = int
        //
        //  double/int   = double
    }
}
